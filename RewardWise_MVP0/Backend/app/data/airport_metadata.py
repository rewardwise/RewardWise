def _dedupe_aliases(aliases: list[str]) -> list[str]:
    seen: set[str] = set()
    result: list[str] = []
    for alias in aliases:
        cleaned = str(alias or "").strip().lower()
        if cleaned and cleaned not in seen:
            seen.add(cleaned)
            result.append(cleaned)
    return result


def _normalize_airport(raw: dict) -> dict:
    iata = str(raw.get("iata", "")).upper().strip()
    name = str(raw.get("name") or f"{iata} Airport").strip()
    city = str(raw.get("city") or iata).strip()
    region = str(raw.get("region") or "").strip()
    country = str(raw.get("country") or "").strip()
    continent = str(raw.get("continent") or "").strip()
    aliases = _dedupe_aliases([
        iata.lower(),
        city.lower(),
        name.lower(),
        *list(raw.get("aliases") or []),
    ])
    return {
        "iata": iata,
        "name": name,
        "city": city,
        "region": region,
        "country": country,
        "continent": continent,
        "aliases": aliases,
    }


def _fallback_airport(code: str) -> dict:
    section = SECTION_BY_CODE.get(code, "")
    country = COUNTRY_OVERRIDES.get(code, section)
    return _normalize_airport({
        "iata": code,
        "name": f"{code} Airport",
        "city": code,
        "region": section,
        "country": country,
        "continent": "",
        "aliases": [code.lower()],
    })


def _build_airports() -> list[dict]:
    by_code: dict[str, dict] = {}
    ordered_codes: list[str] = []

    # Preserve curated order first so option lists are human-friendly. For
    # example, Russia should show the major Moscow airports before obscure
    # fallback codes, while still covering every validator code below.
    for airport in CURATED_AIRPORTS:
        normalized = _normalize_airport(airport)
        code = normalized["iata"]
        if code in VALID_AIRPORT_CODES:
            if code not in by_code:
                ordered_codes.append(code)
            by_code[code] = normalized

    for code in sorted(VALID_AIRPORT_CODES):
        if code not in by_code:
            by_code[code] = _fallback_airport(code)
            ordered_codes.append(code)

    return [by_code[code] for code in ordered_codes]


AIRPORTS = _build_airports()


def validate_airport_metadata_coverage() -> tuple[bool, set[str]]:
    """Return whether every validator code has a metadata row, plus missing codes."""
    metadata_codes = {airport["iata"] for airport in AIRPORTS}
    missing = set(VALID_AIRPORT_CODES) - metadata_codes
    return not missing, missing
