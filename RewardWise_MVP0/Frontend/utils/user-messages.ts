export function mapSupabaseSignInError(raw: string | undefined): string {
	const trimmed = raw?.trim();
	if (!trimmed) return LOGIN_GENERIC_FAIL;
	const m = trimmed.toLowerCase();
	if (
		m.includes("invalid login") ||
		m.includes("invalid_credentials") ||
		m === "invalid email or password" ||
		m.includes("wrong password") ||
		(m.includes("email") &&
			m.includes("password") &&
			(m.includes("invalid") || m.includes("incorrect")))
	) {
		return LOGIN_BAD_CREDENTIALS;
	}
	if (
		m.includes("email not confirmed") ||
		m.includes("not verified") ||
		m.includes("confirm your email")
	) {
		return LOGIN_EMAIL_NOT_CONFIRMED;
	}
	if (
		m.includes("too many requests") ||
		m.includes("rate limit") ||
		m.includes("too_many")
	) {
		return PAY_RATE_LIMIT_SHORT;
	}
	if (trimmed.length > 200) return LOGIN_GENERIC_FAIL;
	return trimmed;
}

export function mapPasswordResetRequestError(raw: string | undefined): string {
	const m = (raw || "").toLowerCase();
	if (
		m.includes("rate") ||
		m.includes("too many") ||
		m.includes("too_many")
	) {
		return PAY_RATE_LIMIT_SHORT;
	}
	if (raw && raw.length < 160) {
		if (
			m.includes("invalid") &&
			(m.includes("email") || m.includes("address"))
		) {
			return "We don’t recognize that email. Check for a typo, or sign up first.";
		}
		return raw;
	}
	return FORGOT_PASSWORD_GENERIC;
}
